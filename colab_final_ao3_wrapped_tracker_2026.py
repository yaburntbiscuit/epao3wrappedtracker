def draw_centered_text(draw, box, text, font, fill):
    x1, y1, x2, y2 = box
    bbox = draw.textbbox((0, 0), text, font=font)
    w = bbox[2] - bbox[0]
    h = bbox[3] - bbox[1]
    x = x1 + (x2 - x1 - w) // 2
    y = y1 + (y2 - y1 - h) // 2
    draw.text((x, y), text, font=font, fill=fill)


def draw_wrapped_centered_text(draw, box, text, font, fill, line_spacing=10):
    """
    Draws text wrapped and centered inside a bounding box.
    box = (x1, y1, x2, y2)
    """
    x1, y1, x2, y2 = box
    max_width = x2 - x1
    max_height = y2 - y1

    words = text.split()
    lines = []
    current_line = ""

    # Build lines word by word
    for word in words:
        test_line = current_line + (" " if current_line else "") + word
        bbox = draw.textbbox((0, 0), test_line, font=font)
        line_width = bbox[2] - bbox[0]

        if line_width <= max_width:
            current_line = test_line
        else:
            lines.append(current_line)
            current_line = word

    if current_line:
        lines.append(current_line)

    # Measure total height of all lines
    line_heights = []
    total_text_height = 0

    for line in lines:
        bbox = draw.textbbox((0, 0), line, font=font)
        h = bbox[3] - bbox[1]
        line_heights.append(h)
        total_text_height += h

    total_text_height += line_spacing * (len(lines) - 1)

    # Starting Y so the block is vertically centered
    y = y1 + (max_height - total_text_height) // 2

    # Draw each line centered horizontally
    for line, h in zip(lines, line_heights):
        bbox = draw.textbbox((0, 0), line, font=font)
        w = bbox[2] - bbox[0]
        x = x1 + (max_width - w) // 2
        draw.text((x, y), line, font=font, fill=fill)
        y += h + line_spacing

