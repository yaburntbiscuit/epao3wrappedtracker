def parse_comma_separated_string(s):
    """Helper to parse a comma-separated string into a list, stripping whitespace."""
    if not s or s.strip() == '':
        return []
    return [item.strip() for item in s.split(',') if item.strip()]

def add_manual_fic_to_tracker(button):
    with manual_output_widget:
        manual_output_widget.clear_output()
        print("Adding fic manually...")

        global fic_tracker_df, tracker_file_path, list_columns

        fic_details = {
            'url': manual_url_input.value.strip(),
            'title': manual_title_input.value.strip(),
            'word_count': manual_word_count_input.value,
            'authors': parse_comma_separated_string(manual_authors_input.value),
            'ratings': parse_comma_separated_string(manual_ratings_input.value),
            'archive_warnings': parse_comma_separated_string(manual_warnings_input.value),
            'category': parse_comma_separated_string(manual_category_input.value),
            'fandoms': parse_comma_separated_string(manual_fandoms_input.value),
            'relationships': parse_comma_separated_string(manual_relationships_input.value),
            'characters': parse_comma_separated_string(manual_characters_input.value),
            'free_form_tags': parse_comma_separated_string(manual_freeform_tags_input.value)
        }

        if not fic_details['url'] or not fic_details['title']:
            print("Error: URL and Title are required fields.")
            return

        # Use reindex to ensure the Series has all columns of fic_tracker_df
        new_fic_series = pd.Series(fic_details).reindex(fic_tracker_df.columns)

        # Append new Series as a new row to the DataFrame
        fic_tracker_df = pd.concat([fic_tracker_df, pd.DataFrame([new_fic_series])], ignore_index=True)

        # Save the updated DataFrame to CSV
        df_to_save = fic_tracker_df.copy()
        for col in list_columns:
            if col in df_to_save.columns:
                df_to_save[col] = df_to_save[col].apply(lambda x: str(x) if isinstance(x, list) else x)

        df_to_save.to_csv(tracker_file_path, index=False)
        print(f"Successfully added fic '{fic_details['title']}' to tracker. Updated tracker saved to {tracker_file_path}.")

        # Clear input fields after successful submission
        manual_url_input.value = ''
        manual_title_input.value = ''
        manual_word_count_input.value = 0
        manual_authors_input.value = ''
        manual_ratings_input.value = ''
        manual_warnings_input.value = ''
        manual_category_input.value = ''
        manual_fandoms_input.value = ''
        manual_relationships_input.value = ''
        manual_characters_input.value = ''
        manual_freeform_tags_input.value = ''

