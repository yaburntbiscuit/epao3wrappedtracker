import requests
from bs4 import BeautifulSoup

def extract_fic_data(url):
    """
    Extracts detailed information from an AO3 fanfiction URL.

    Args:
        url (str): The URL of the AO3 fanfiction.

    Returns:
        dict: A dictionary containing extracted fanfiction data.
              Returns an empty dictionary if extraction fails.
    """
    fic_data = {
        'url': url,
        'title': None, # Added for title extraction
        'word_count': None,
        'ratings': [],
        'archive_warnings': [],
        'category': [],
        'fandoms': [],
        'relationships': [],
        'characters': [],
        'free_form_tags': [],
        'authors': [] # Added authors key
    }

    try:
        response = requests.get(url)
        response.raise_for_status()  # Raise an HTTPError for bad responses (4xx or 5xx)
        soup = BeautifulSoup(response.text, 'html.parser')

        # Extract Title
        title_h2 = soup.find('h2', class_='title')
        if title_h2:
            fic_data['title'] = title_h2.get_text(strip=True)

        # Find the work meta group where most of the data is located
        work_meta = soup.find('dl', class_='work meta group')

        if work_meta:
            # Helper function to extract list items from a dd tag
            def extract_list_items(dd_tag, class_name):
                items = []
                dd_element = dd_tag.find('dd', class_=class_name)
                if dd_element:
                    for li in dd_element.find_all('li'):
                        items.append(li.get_text(strip=True))
                return items

            # Word Count
            words_dd = work_meta.find('dd', class_='words')
            if words_dd:
                try:
                    fic_data['word_count'] = int(words_dd.get_text(strip=True).replace(',', ''))
                except ValueError:
                    fic_data['word_count'] = None

            # Ratings
            fic_data['ratings'] = extract_list_items(work_meta, 'rating')

            # Archive Warnings
            fic_data['archive_warnings'] = extract_list_items(work_meta, 'warning')

            # Category (e.g., M/M, F/F)
            fic_data['category'] = extract_list_items(work_meta, 'category')

            # Fandoms
            fic_data['fandoms'] = extract_list_items(work_meta, 'fandom')

            # Relationships
            fic_data['relationships'] = extract_list_items(work_meta, 'relationship')

            # Characters
            fic_data['characters'] = extract_list_items(work_meta, 'character')

            # Other General Tags (Freeform)
            fic_data['free_form_tags'] = extract_list_items(work_meta, 'freeform')

        # Extract Author(s)
        # Changed from soup.find('div', class_='byline') to soup.find(class_='byline') for broader matching
        byline_element = soup.find(class_='byline')
        if byline_element:
            authors = byline_element.find_all('a', rel='author')
            for author_tag in authors:
                fic_data['authors'].append(author_tag.get_text(strip=True))

    except requests.exceptions.RequestException as e:
        print(f"Request failed for {url}: {e}")
    except Exception as e:
        print(f"An error occurred during extraction for {url}: {e}")

    return fic_data

