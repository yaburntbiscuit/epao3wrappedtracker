def add_fic_to_tracker(url):
    """
    Extracts fanfiction data from a given URL, adds it to the global fic_tracker_df,
    and saves the updated DataFrame to Google Drive.

    Args:
        url (str): The URL of the AO3 fanfiction to track.
    """
    global fic_tracker_df, tracker_file_path, list_columns

    # 2. Extract fic data
    print(f"Extracting data for: {url}")
    fic_details = extract_fic_data(url)

    if not fic_details or fic_details.get('word_count') is None:
        print(f"Could not extract data for {url}. It might be invalid or inaccessible. Skipping.")
        return

    # 3. Convert dictionary to pandas Series and ensure column order
    # Use reindex to ensure the Series has all columns of fic_tracker_df, filling missing with NaN
    new_fic_series = pd.Series(fic_details).reindex(fic_tracker_df.columns)

    # 4. Append new Series as a new row to the DataFrame
    # Using pd.concat for appending a single row (recommended over df.append for newer pandas versions)
    fic_tracker_df = pd.concat([fic_tracker_df, pd.DataFrame([new_fic_series])], ignore_index=True)

    # 5. Save the updated DataFrame to CSV
    # Convert list columns back to string representations for CSV saving
    df_to_save = fic_tracker_df.copy()
    for col in list_columns:
        if col in df_to_save.columns:
            df_to_save[col] = df_to_save[col].apply(lambda x: str(x) if isinstance(x, list) else x)

    df_to_save.to_csv(tracker_file_path, index=False)
    print(f"Successfully added fic from {url} to tracker. Updated tracker saved to {tracker_file_path}.")

